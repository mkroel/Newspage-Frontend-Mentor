function openMenu() {
    var x = document.getElementById("menu");
        
        if (x.style.display === "none") {
        x.style.display = "block";
        } else {
        x.style.display = "none";
        }
}

function closeMenu() {
    var y = document.getElementById("menu");

    if (y.style.display === "none") {
        y.style.display = "block";
        } else {
        y.style.display = "none";
        }
}